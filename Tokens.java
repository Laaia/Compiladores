class Tokens{
  private int id, posicao;
  private String tipo;

  public void setId(int a){
    this.id = a;
  }
  public int getId(){
    return this.id;
  }
  public void setPosicao(int a){
    this.posicao = a;
  }
  public int getPosicao(){
    return this.posicao;
  }
  public void setTipo(String a){
    this.tipo = a;
  }
  public String getTipo(){
    return this.tipo;
  }
}
