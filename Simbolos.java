class Simbolos{
  private int id;
  private String nome, categoria, tipo, escopo;

  public void setId(int a){
    this.id = a;
  }
  public int getId(){
    return this.id;
  }
  public void setNome(String a){
    this.nome = a;
  }
  public String getNome(){
    return this.nome;
  }
  public void setCategoria(String a){
    this.categoria = a;
  }
  public String getCategoria(){
    return this.categoria;
  }
  public void setTipo(String a){
    this.tipo = a;
  }
  public String getTipo(){
    return this.tipo;
  }
  public void setEscopo(String a){
    this.escopo = a;
  }
  public String getEscopo(){
    return this.escopo;
  }
}
